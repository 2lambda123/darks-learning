package darks.learning.exceptions;

public class TrainingException extends LearningException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7941341353467687996L;

	public TrainingException()
	{
		super();
	}

	public TrainingException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public TrainingException(String message)
	{
		super(message);
	}

	public TrainingException(Throwable cause)
	{
		super(cause);
	}

	
}
