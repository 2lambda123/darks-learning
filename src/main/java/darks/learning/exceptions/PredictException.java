package darks.learning.exceptions;

public class PredictException extends LearningException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7941341353467687996L;

	public PredictException()
	{
		super();
	}

	public PredictException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public PredictException(String message)
	{
		super(message);
	}

	public PredictException(Throwable cause)
	{
		super(cause);
	}

	
}
